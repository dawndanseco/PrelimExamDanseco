class triangle:
    def __init__(self, height, base):
        self.height = height
        self.base = base

    def area (self):
        return self.height(1/2) * self.base 

    def perimeter (self):
        return self.height * self.base

output = triangle (5,10)
print(f'The Area is: {output.area}, while the Perimeter is: {output.perimeter}')

